package leitorxml;


public class Produto {
    

private String Nome;
private int NCM;
private int Quant;
private float Vvenda;
private float VUnitario;


  

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public int getNCM() {
        return NCM;
    }

    public void setNCM(int NCM) {
        this.NCM = NCM;
    }

   

    public long getQuant() {
        return  Quant;
    }

    public void setQuant(int Quant) {
        this.Quant = Quant;
    }

    public float getVvenda() {
        return Vvenda;
    }

    public void setVvenda(float Vvenda) {
        this.Vvenda = Vvenda;
    }

    public float getVUnitario() {
        return VUnitario;
    }

    public void setVUnitario(float VUnitario) {
        this.VUnitario = VUnitario;
    }

   


    
    
    
}
